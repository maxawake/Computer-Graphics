Param([Parameter(Mandatory=$True)][string]$PluginName)

$PathParts = $PluginName.Split("\\/")
$currDir="."
$subDir="../.."
$PName = $PathParts[$PathParts.Length - 1]
if ($PathParts.Length -gt 1) {
    $PRest = $PathParts[0..($PathParts.Length - 2)]
    foreach ($part in $PRest) {
        $currDir += "/$part"
        if (! (Test-Path $currDir)) {
            mkdir $currDir
        }
        $subdir += "/.."
    }
}

Copy-Item Template $PluginName -Recurse
$SolGuid = [System.Guid]::NewGuid().toString()
$ProjGuid = [System.Guid]::NewGuid().toString()

#foreach ($file in gi "$PluginName\*" -include "Template.*", "Makefile") {
foreach ($file in gi "$PluginName\*") {
    $newfile = $file.DirectoryName + "\" + ($file.Name -replace "Template", "$PName")
    Rename-Item -path $file.FullName -newname $newfile
    $text = (((Get-Content $newfile) -replace "\[Template\]", "$PName") -replace "\[ProjectGuid\]", "$ProjGuid") -replace "\[SolutionGuid\]", "$SolGuid" -replace "\[OGL4CoreDirectory\]", "$subDir"
    Set-Content -path $newfile -value $text
}
